void	ft_rev_int_tab(int *tab, int size)
{
	int			i;
	char		tmp;

	i = -1;
	while (i < size)
	{
		i++;
		size--;
		tmp = tab[i];
		tab[i] = tab[size];
		tab[size] = tmp;
	}
}
